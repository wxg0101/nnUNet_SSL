    def run_iteration(self, data_generator, data_generator_2,do_backprop=True, run_online_evaluation=False,iter_num=0):
        """
        gradient clipping improves training stability

        :param data_generator:
        :param do_backprop:
        :param run_online_evaluation:
        :return:
        """
        def update_ema_variables(model, ema_model, alpha, global_step):
            # Use the true average until the exponential average is more correct
            alpha = min(1 - 1 / (global_step + 1), alpha)
            for ema_param, param in zip(ema_model.parameters(), model.parameters()):
                ema_param.data.mul_(alpha).add_(1 - alpha, param.data)
        def get_current_consistency_weight(epoch):
            def sigmoid_rampup(current, rampup_length):
                """Exponential rampup from https://arxiv.org/abs/1610.02242"""
                if rampup_length == 0:
                    return 1.0
                else:
                    current = np.clip(current, 0.0, rampup_length)
                    phase = 1.0 - current / rampup_length
                    return float(np.exp(-5.0 * phase * phase))
            # Consistency ramp-up from https://arxiv.org/abs/1610.02242
            return 0.1 * sigmoid_rampup(epoch, 60)
        data_dict = next(data_generator)
        data_dict_2 = next(data_generator_2)
        # data_dict_3 = next(data_generator_3)
        # TypeError: 'bool' object is not an iterator
        data = data_dict['data']
        target = data_dict['target']
        unlabeled = data_dict_2['data']#TODO:增加无标签数据的读取√
        
        # ema_inputs = data_dict_3['data']

        data = maybe_to_torch(data)
        target = maybe_to_torch(target)
        unlabeled = maybe_to_torch(unlabeled)
        noise = torch.clamp(torch.randn_like(
                unlabeled) * 0.1, -0.1, 0.2)
        ema_inputs = unlabeled.rot90(k=1,dims=[2,3]) ######+ noise
        if torch.cuda.is_available():
            data = to_cuda(data)
            target = to_cuda(target)
            unlabeled = to_cuda(unlabeled)
            ema_inputs = to_cuda(ema_inputs)
            ######有bug，待解决,RuntimeError: 
            # Sizes of tensors must match except in dimension 1. 
            # Expected size 96 but got size 95 for tensor number 1 in the list.


        self.optimizer.zero_grad()

        if self.fp16:
            with autocast():
                output = self.network(data)
                del data
                l = self.loss(output, target)+1
                with torch.no_grad():
                    ema_output = self.ema_model(ema_inputs)
                    del ema_inputs
                supervised_loss = l ###0.5需要么？
                consistency_weight = get_current_consistency_weight(iter_num//250)

                if iter_num < 1250:
                    consistency_loss = 0.0
                else:
                    output_unlabel = self.network(unlabeled)
                    consistency_loss = torch.mean(
                        (output_unlabel[0] - ema_output[0].rot90(k=-1,dims=[2,3]))**2
                    )
                # self.print_to_log_file("consistency_weight:{%.4f}"%consistency_weight)
                # self.print_to_log_file("consistency_loss:{}"%consistency_loss)
                loss = supervised_loss + consistency_weight*consistency_loss
                unsupervised_loss = consistency_weight*consistency_loss

            if do_backprop:
                self.amp_grad_scaler.scale(loss).backward()
                self.amp_grad_scaler.unscale_(self.optimizer)
                torch.nn.utils.clip_grad_norm_(self.network.parameters(), 12)
                self.amp_grad_scaler.step(self.optimizer)
                self.amp_grad_scaler.update()
                update_ema_variables(self.network, self.ema_model, 0.99, iter_num)
        else:
            print("fp16 is not used")


        if run_online_evaluation:
            self.run_online_evaluation(output, target)

        del target
        if unsupervised_loss == 0.0:
            return loss.detach().cpu().numpy(),unsupervised_loss,supervised_loss.detach().cpu().numpy(),consistency_weight
        else:
            return loss.detach().cpu().numpy(),unsupervised_loss.detach().cpu().numpy(),supervised_loss.detach().cpu().numpy(),consistency_weight
